    //REGISTER PERSON IN DATA BASE AND SHOW THIS IN TABLE
    private void btnaddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnaddActionPerformed
        Connection conexion = null;
        try {
            conexion = Conexion.getConnection();
            if (conexion.getAutoCommit()) {
                conexion.setAutoCommit(false);
            }
            //CREATE NEW PERSON WHITH TEXT FILE
            PersonaDao personaDao = new PersonaDaoJDBC(conexion);
            PersonaDTO nuevaPersona = new PersonaDTO();
            //VALUES FROM USERINTERFACE
            nuevaPersona.setNombre(txtNombre.getText());
            nuevaPersona.setApellido(txtApellido.getText());
            nuevaPersona.setEmail(txtEmail.getText());
            nuevaPersona.setTelefono(txtTelefono.getText());
            personaDao.insertar(nuevaPersona);
            //TABLE FOR COMPROBATE
            List<PersonaDTO> personas = personaDao.seleccionar();
            for (PersonaDTO persona : personas) {
                System.out.println("Persona DTO: " + persona);
            }
            conexion.commit();
            System.out.println("exitoso");
        } catch (Exception e) {
            e.printStackTrace(System.out);
            System.out.println("entramos el rollback");
            try {
                conexion.rollback();
            } catch (SQLException ex) {
                ex.printStackTrace(System.out);
            }
        }
    }//GEN-LAST:event_btnaddActionPerformed

