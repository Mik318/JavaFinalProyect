    public void showtable() {
        Connection conexion = null;
        try {
            conexion = Conexion.getConnection();
            if (conexion.getAutoCommit()) {
                conexion.setAutoCommit(false);
            }
            //CREATE NEW PERSON WHITH TEXT FILE
            PersonaDao personaDao = new PersonaDaoJDBC(conexion);
            List<PersonaDTO> personas = personaDao.seleccionar();
            String array[][] = new String[personas.size()][4];
            for (int i = 0; i < personas.size(); i++) {
                array[i][0] = personas.get(i).getNombre();
                array[i][1] = personas.get(i).getApellido();
                array[i][2] = personas.get(i).getEmail();
                array[i][3] = personas.get(i).getTelefono();
            }

            jTablePersonaDao.setModel(new javax.swing.table.DefaultTableModel(
                    array,
                    new String[]{
                        "Nombre", "Apellido", "Email", "Telefono"
                    }
            ));
            conexion.commit();
            System.out.println("exitoso");
        } catch (Exception e) {
            e.printStackTrace(System.out);
            System.out.println("entramos el rollback");
            try {
                conexion.rollback();
            } catch (SQLException ex) {
                ex.printStackTrace(System.out);
            }
        }

    }

